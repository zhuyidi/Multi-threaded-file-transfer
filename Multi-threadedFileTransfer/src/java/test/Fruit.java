public class Fruit {
    private float apple;
    private float banana;
    private float peach;

    public Fruit() { }

    public float getApple() {
        return apple;
    }

    public void setApple(float apple) {
        this.apple = apple;
    }

    public float getBanana() {
        return banana;
    }

    public void setBanana(float banana) {
        this.banana = banana;
    }

    public float getPeach() {
        return peach;
    }

    public void setPeach(float peach) {
        this.peach = peach;
    }
}
